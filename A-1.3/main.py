"""
A-1.3 - Operaciones aritméticas (II). Calculadora simple
Se debe mostrar un mensaje que pida un número por teclado y a continuación otro segundo número.
Mostrar el resultado obtenido de sumar, restar, multiplicar y dividir ambos números.
"""

numero1 = int(input('Dime un numero: '))
numero2 = int(input('Dime otro numero: '))

print(f'Sumado: {numero1 + numero2}')
print(f'Multiplicado: {numero1 * numero2}')
print(f'Dividido: {numero1 / numero2}')
print(f'Restado: {numero1 - numero2}')
