"""
A-1.5 - Operaciones aritméticas (IV). Promedio de tres números
Escribir un programa que solicite al usuario ingresar tres números y calcule el promedio de los tres.

Ejemplo de ejecución:

Primer número: 8.5
Segundo número: 10
Tercer número: 5.5
El promedio de los tres es 8.0
"""


numero1 = float(input('Numero 1: '))
numero2 = float(input('Numero 2: '))
numero3 = float(input('Numero 3: '))

print(f'El promedio de los tres es {(numero1 + numero2 + numero3)/3}')
